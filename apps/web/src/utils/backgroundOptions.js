// Utility function to convert hex to HSL
function hexToHsl(hex) {
  const r = parseInt(hex.slice(1, 3), 16) / 255;
  const g = parseInt(hex.slice(3, 5), 16) / 255;
  const b = parseInt(hex.slice(5, 7), 16) / 255;

  const max = Math.max(r, g, b);
  const min = Math.min(r, g, b);
  let h, s, l = (max + min) / 2;

  if (max === min) {
    h = s = 0;
  } else {
    const d = max - min;
    s = l > 0.5 ? d / (2 - max - min) : d / (max + min);
    switch (max) {
      case r: h = (g - b) / d + (g < b ? 6 : 0); break;
      case g: h = (b - r) / d + 2; break;
      case b: h = (r - g) / d + 4; break;
    }
    h /= 6;
  }

  return [h * 360, s * 100, l * 100];
}

// Utility function to convert HSL to hex
function hslToHex(h, s, l) {
  l /= 100;
  const a = s * Math.min(l, 1 - l) / 100;
  const f = n => {
    const k = (n + h / 30) % 12;
    const color = l - a * Math.max(Math.min(k - 3, 9 - k, 1), -1);
    return Math.round(255 * color).toString(16).padStart(2, '0');
  };
  return `#${f(0)}${f(8)}${f(4)}`;
}

// Generate brand-based gradients from a brand color
export function generateBrandGradients(brandColor) {
  if (!brandColor) return [];

  const [h, s, l] = hexToHsl(brandColor);
  
  const brandGradients = [
    // Main brand gradient (default)
    `linear-gradient(135deg, ${brandColor} 0%, ${hslToHex(h, Math.max(s - 20, 20), Math.max(l - 15, 15))} 100%)`,
    
    // Light variations - More pastel and bright options
    `linear-gradient(135deg, ${hslToHex(h, Math.max(s - 30, 15), Math.min(l + 35, 92))} 0%, ${hslToHex(h, Math.max(s - 20, 20), Math.min(l + 20, 85))} 100%)`,
    `linear-gradient(135deg, ${hslToHex(h, Math.max(s - 25, 20), Math.min(l + 30, 88))} 0%, ${brandColor} 100%)`,
    `linear-gradient(135deg, ${brandColor} 0%, ${hslToHex(h, Math.max(s - 35, 10), Math.min(l + 40, 95))} 100%)`,
    `linear-gradient(135deg, ${hslToHex(h, Math.max(s - 20, 25), Math.min(l + 25, 87))} 0%, ${hslToHex(h, Math.max(s - 10, 30), Math.min(l + 15, 80))} 100%)`,
    
    // Medium variations
    `linear-gradient(135deg, ${hslToHex(h, Math.max(s - 10, 20), Math.min(l + 10, 75))} 0%, ${brandColor} 100%)`,
    `linear-gradient(135deg, ${brandColor} 0%, ${hslToHex(h, Math.max(s - 15, 15), Math.min(l + 15, 80))} 100%)`,
    
    // Darker variations
    `linear-gradient(135deg, ${hslToHex(h, Math.min(s + 10, 90), Math.max(l - 25, 10))} 0%, ${brandColor} 100%)`,
    `linear-gradient(135deg, ${brandColor} 0%, ${hslToHex(h, Math.min(s + 15, 95), Math.max(l - 30, 5))} 100%)`,
    
    // Complementary light variations
    `linear-gradient(135deg, ${hslToHex(h, Math.max(s - 20, 20), Math.min(l + 20, 85))} 0%, ${hslToHex((h + 180) % 360, Math.max(s - 20, 20), Math.min(l + 20, 85))} 100%)`,
    `linear-gradient(135deg, ${hslToHex((h + 120) % 360, Math.max(s - 25, 15), Math.min(l + 25, 87))} 0%, ${hslToHex((h + 240) % 360, Math.max(s - 25, 15), Math.min(l + 25, 87))} 100%)`,
    
    // Analogous light variations
    `linear-gradient(135deg, ${hslToHex((h + 30) % 360, Math.max(s - 20, 20), Math.min(l + 20, 85))} 0%, ${hslToHex((h - 30 + 360) % 360, Math.max(s - 20, 20), Math.min(l + 20, 85))} 100%)`,
    
    // 4 Additional Beautiful Brand Gradients
    // Radial gradient effect
    `linear-gradient(135deg, ${brandColor} 0%, ${hslToHex(h, Math.min(s + 20, 95), Math.max(l - 10, 15))} 50%, ${hslToHex(h, Math.max(s - 15, 20), Math.min(l + 30, 90))} 100%)`,
    
    // Sunset/sunrise effect with brand color
    `linear-gradient(135deg, ${hslToHex((h + 45) % 360, Math.max(s - 10, 30), Math.min(l + 25, 85))} 0%, ${brandColor} 50%, ${hslToHex((h - 45 + 360) % 360, Math.max(s - 15, 25), Math.min(l + 20, 80))} 100%)`,
    
    // Monochromatic depth gradient
    `linear-gradient(135deg, ${hslToHex(h, Math.max(s - 40, 10), Math.min(l + 45, 95))} 0%, ${hslToHex(h, Math.max(s - 20, 20), Math.min(l + 20, 85))} 30%, ${brandColor} 70%, ${hslToHex(h, Math.min(s + 10, 90), Math.max(l - 20, 10))} 100%)`,
    
    // Sophisticated triadic blend
    `linear-gradient(135deg, ${brandColor} 0%, ${hslToHex((h + 60) % 360, Math.max(s - 15, 25), Math.min(l + 15, 80))} 50%, ${hslToHex((h + 300) % 360, Math.max(s - 10, 30), Math.min(l + 10, 75))} 100%)`,
  ];

  return brandGradients;
}

// Get default brand gradient (first one from the generated list)
export function getDefaultBrandGradient(brandColor) {
  if (!brandColor) return null;
  const gradients = generateBrandGradients(brandColor);
  return gradients[0]; // Return the main brand gradient
}

// Get all gradient groups including brand gradients
export function getAllGradientGroups(brandColor = null) {
  const orderedGroups = {};
  
  // Add brand colors first if available
  if (brandColor) {
    orderedGroups.brand = {
      name: "Brand Colors",
      gradients: generateBrandGradients(brandColor)
    };
  }
  
  // Add all other groups in order
  orderedGroups.aurora = gradientGroups.aurora;
  orderedGroups.purple = gradientGroups.purple;
  orderedGroups.blue = gradientGroups.blue;
  orderedGroups.green = gradientGroups.green;
  orderedGroups.warm = gradientGroups.warm;
  orderedGroups.neutral = gradientGroups.neutral;
  
  return orderedGroups;
}
